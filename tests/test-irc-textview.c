int
main (int argc, char **argv)
{
	gtk_init (&argc, &argv);

	GtkWidget *win = gtk_window_new (GTK_WINDOW_TOPLEVEL);
	GtkWidget *sw = gtk_scrolled_window_new (NULL, NULL);
	IrcTextview *view = irc_textview_new ();
	gtk_container_add (GTK_CONTAINER(win), sw);
	gtk_container_add (GTK_CONTAINER(sw), GTK_WIDGET(view));
	gtk_window_set_default_size (GTK_WINDOW(win), 800, 600);
	gtk_widget_show_all (win);

	g_signal_connect (win, "delete-event", G_CALLBACK(on_delete), NULL);


	irc_textview_append_text (view, "Testing colors:", 0);
	for (guint i = 0; i < G_N_ELEMENTS(color_lines); ++i)
	{
		irc_textview_append_text (view, color_lines[i], 0);
	}

	irc_textview_append_text (view, "", 0);

  	irc_textview_append_text (view, "Testing links:", 0);
	for (guint i = 0; i < G_N_ELEMENTS(link_lines); ++i)
	{
		irc_textview_append_text (view, link_lines[i], 0);
	}

	irc_textview_append_text (view, "", 0);

  	irc_textview_append_text (view, "Testing Other Attrs:", 0);
	for (guint i = 0; i < G_N_ELEMENTS(attr_lines); ++i)
	{
		irc_textview_append_text (view, attr_lines[i], 0);
	}

	gtk_main ();
}
